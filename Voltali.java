	@Override
	public void subire(Pokemon adversaire, int degats){
		/**
	 	 * Voltali a 30% de chances de paralyser son adversaire
	 	 * à chaque fois qu'il subit des dégats
	 	 */
		
		// on appelle la fonction subire  et on la complete avec le talent du pokemon
		super.subire(adversaire, degats);

		if(Math.random() > 0.3){					//	probabilité de 30%
			adversaire.paralyse += 1;				//	on augmente paralyse de 1 (si le pokemon est paralysé, il le reste pour un tour de plus)
		}
	}

